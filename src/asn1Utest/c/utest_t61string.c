#include "utest_asn1.h"

static T61String_t *test_t61string_create(void)
{
    uint8_t encode[15] = {
        0x14, 0x0d,
        0x54, 0x65, 0x73, 0x74,
        0x20, 0x6d, 0x65, 0x73,
        0x73, 0x61, 0x67, 0x65,
        0x2e
    };

    T61String_t *t61string = NULL;

    ASN_EXECUTE(ber_decode(0, &T61String_desc, (void *)&t61string, encode, sizeof(encode)));

    ASSERT_NOT_NULL(t61string);
cleanup:
    return t61string;
}

void test_t61string_alloc_free(void)
{
    T61String_t *t61string = NULL;

    ASSERT_ASN_ALLOC(t61string);
    ASSERT_NOT_NULL(t61string);
cleanup:

    ASN_FREE(&T61String_desc, t61string);
}

void test_t61string_der_encode(void)
{
    ByteArray *actual = NULL;
    ByteArray *expected = NULL;
    uint8_t exp[15] = {
        0x14, 0x0d,
        0x54, 0x65, 0x73, 0x74,
        0x20, 0x6d, 0x65, 0x73,
        0x73, 0x61, 0x67, 0x65,
        0x2e
    };

    T61String_t *t61string = NULL;

    t61string = test_t61string_create();
    ASSERT_NOT_NULL(t61string);
    ASSERT_NOT_NULL(expected = ba_alloc_from_uint8(exp, sizeof(exp)));

    ASSERT_RET_OK(asn_encode_ba(&T61String_desc, t61string, (void *)&actual));
    ASSERT_NOT_NULL(actual);

    ASSERT_EQUALS_BA(expected, actual);

cleanup:
    BA_FREE(expected, actual);
    ASN_FREE(&T61String_desc, t61string);
}

