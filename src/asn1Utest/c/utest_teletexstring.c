#include "utest_asn1.h"

static TeletexString_t *test_teletexstring_create(void)
{
    uint8_t encode[15] = {
        0x14, 0x0d,
        0x54, 0x65, 0x73, 0x74,
        0x20, 0x6d, 0x65, 0x73,
        0x73, 0x61, 0x67, 0x65,
        0x2e
    };

    TeletexString_t *teletexstring = NULL;

    ASN_EXECUTE(ber_decode(0, &TeletexString_desc, (void *)&teletexstring, encode, sizeof(encode)));

    ASSERT_NOT_NULL(teletexstring);
cleanup:
    return teletexstring;
}

void test_teletexstring_alloc_free(void)
{
    TeletexString_t *teletexstring = NULL;

    ASSERT_ASN_ALLOC(teletexstring);
    ASSERT_NOT_NULL(teletexstring);
cleanup:

    ASN_FREE(&TeletexString_desc, teletexstring);
}

void test_teletexstring_der_encode(void)
{
    ByteArray *actual = NULL;
    ByteArray *expected = NULL;
    uint8_t exp[15] = {
        0x14, 0x0d,
        0x54, 0x65, 0x73, 0x74,
        0x20, 0x6d, 0x65, 0x73,
        0x73, 0x61, 0x67, 0x65,
        0x2e
    };

    TeletexString_t *teletexstring = NULL;

    teletexstring = test_teletexstring_create();
    ASSERT_NOT_NULL(teletexstring);
    ASSERT_NOT_NULL(expected = ba_alloc_from_uint8(exp, sizeof(exp)));

    ASSERT_RET_OK(asn_encode_ba(&TeletexString_desc, teletexstring, (void *)&actual));
    ASSERT_NOT_NULL(actual);

    ASSERT_EQUALS_BA(expected, actual);

cleanup:
    BA_FREE(expected, actual);
    ASN_FREE(&TeletexString_desc, teletexstring);
}

