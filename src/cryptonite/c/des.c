static void des_xor(void *arg1, void *arg2, void *out)
{
    uint64_t *a1 = (uint64_t *) arg1;
    uint64_t *a2 = (uint64_t *) arg2;
    uint64_t *o = (uint64_t *) out;

    o[0] = a1[0] ^ a2[0];
}

